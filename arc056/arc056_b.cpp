class UnionFind {
public:
    // 親の番号を格納する。親だった場合-size
    vector<int> parents;

    UnionFind(int n) {
        parents = vector<int>(n, -1);
    }

    // aがどのグループに属しているか
    int root(int a) {
        if (parents[a] < 0) {
            return a;
        }
        return parents[a] = root(parents[a]);
    }

    int size(int a) {
        return -parents[root(a)];
    }

    // aとbをくっつける
    bool connect(int a, int b) {
        int ra = root(a);
        int rb = root(b);
        if (ra == rb) {
            return false;
        }
        // 大きいほうにA
        if (size(ra) < size(rb)) {
            swap(ra, rb);
        }
        parents[ra] += parents[rb];
        parents[rb] = ra;
        return true;
    }

    bool is_union(int a, int b) {
        int ra = root(a);
        int rb = root(b);
        return ra == rb;
    }
};


int main() {
    int n, m, s;
    cin >> n >> m >> s;

    s--;

    vector<vector<int>> edges(n);
    rep(i, m) {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        if (a > b) swap(a, b);
        edges[a].push_back(b);
    }

    UnionFind uf(n);
    vector<int> ans;
    for (int i = n - 1; i >= 0; i--) {
        for (int j : edges[i]) uf.connect(j, i);
        if (uf.is_union(s, i)) ans.push_back(i);
    }

    reverse(ans.begin(), ans.end());

    for (int i : ans) cout << i + 1 << endl;

}
