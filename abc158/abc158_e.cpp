    rep(i, n) {
        int x = s[i] - '0';
        mint f = mint(x) * mint(10).pow(i);
        cs.set(i, f.x);
    }
    cs.build();

    vector<ll> md(p);
    md[0] = 1;

    rep(i, n) {
        md[cs.getSum(i) % p]++;
    }

    ll ans = 0;
    rep(i, p) {
        ll k = md[i];
        ll now = k * (k - 1) / 2;
        ans += now;
    }
