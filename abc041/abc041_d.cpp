const int mod = 1000000007;

struct mint {
    ll x; // typedef long long ll;
    mint(ll x = 0) : x((x % mod + mod) % mod) {}

    mint &operator+=(const mint a) {
        if ((x += a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator-=(const mint a) {
        if ((x += mod - a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator*=(const mint a) {
        (x *= a.x) %= mod;
        return *this;
    }

    mint operator+(const mint a) const {
        mint res(*this);
        return res += a;
    }

    mint operator-(const mint a) const {
        mint res(*this);
        return res -= a;
    }

    mint operator*(const mint a) const {
        mint res(*this);
        return res *= a;
    }

    mint pow(ll t) const {
        if (!t) return 1;
        mint a = pow(t >> 1);
        a *= a;
        if (t & 1) a *= *this;
        return a;
    }

    // for prime mod
    mint inv() const {
        return pow(mod - 2);
    }

    mint &operator/=(const mint a) {
        return (*this) *= a.inv();
    }

    mint operator/(const mint a) const {
        mint res(*this);
        return res /= a;
    }

    friend std::istream &operator>>(std::istream &in, mint &o) {
        ll a;
        in >> a;
        o = a;
        return in;
    }

    friend std::ostream &operator<<(std::ostream &out, const mint &o) {
        out << o.x;
        return out;
    }

};

int bit_rev(int n, int st, int i) {
    assert((st >> i) & 1);
    return st - (1 << i);
}

mint recursive(int n, vector<vector<int>> &g, int st) {
    if (__builtin_popcount(st) == 1) return 1;
    mint ans = 0;

    vector<bool> is_terminate(n, true);
    rep(i, n) {
        if (!((st >> i) & 1)) continue;
        for (int to : g[i]) {
            if (!((st >> to) & 1)) continue;
            is_terminate[i] = false;
        }
    }

    rep(i, n) {
        if ((st >> i) & 1) {
            if (is_terminate[i]) {
                int next_st = bit_rev(n, st, i);
                mint now = recursive(n, g, next_st);
                ans += now;
            }
        }
    }
    return ans;
}

