class CumulativeSum {
    vector<ll> numbers;
    vector<ll> sums;

public:
    CumulativeSum(int n) {
        numbers.resize(n);
        sums.resize(n);
    }

    void set(int i, ll value) {
        numbers[i] = value;
    }

    ll getSum(int i) {
        if (i == -1) return 0;
        if (i == sums.size()) return 0;
        return sums[i];
    }

    ll getSectionSum(int start, int end) {
        return getSum(end) - getSum(start - 1);
    }

    void calculate() {
        for (int i = 0; i < numbers.size(); i++) {
            sums[i] = getSum(i - 1) + numbers[i];
        }
    }

};

int main() {
    int n;
    cin >> n;

    CumulativeSum cs(n);
    rep(i, n) {
        int a;
        cin >> a;
        cs.set(i, a);
    }

    cs.calculate();

    cout << endl;

    int ans = INF;
    for (int i = 0; i < n - 1; i++) {
        int now = abs(cs.getSum(i) - cs.getSectionSum(i + 1, n - 1));
        cmin(ans, now);
    }
    cout << ans << endl;

}