int main() {
    int n, m;
    cin >> n >> m;

    vector<vector<int>> edges(n);

    rep(i, m) {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        edges[a].push_back(b);
        edges[b].push_back(a);
    }

    vector<int> prev(n, -1);

    struct Q {
        int to;
        int prev;
    };
    queue<Q> q;
    q.push(Q{0, -1});

    while (!q.empty()) {
        Q t = q.front();
        q.pop();
        for (int next : edges[t.to]) {
            if (prev[next] != -1) continue;
            prev[next] = t.to;
            q.push(Q{next, t.prev});
        }
    }

    rep(i, n) {
        if (i == 0) continue;
        cout << prev[i] + 1 << endl;
    }

}
