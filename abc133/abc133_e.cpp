const int mod = 1000000007;

struct mint {
    ll x; // typedef long long ll;
    mint(ll x = 0) : x((x % mod + mod) % mod) {}

    mint &operator+=(const mint a) {
        if ((x += a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator-=(const mint a) {
        if ((x += mod - a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator*=(const mint a) {
        (x *= a.x) %= mod;
        return *this;
    }

    mint operator+(const mint a) const {
        mint res(*this);
        return res += a;
    }

    mint operator-(const mint a) const {
        mint res(*this);
        return res -= a;
    }

    mint operator*(const mint a) const {
        mint res(*this);
        return res *= a;
    }

    mint pow(ll t) const {
        if (!t) return 1;
        mint a = pow(t >> 1);
        a *= a;
        if (t & 1) a *= *this;
        return a;
    }

    // for prime mod
    mint inv() const {
        return pow(mod - 2);
    }

    mint &operator/=(const mint a) {
        return (*this) *= a.inv();
    }

    mint operator/(const mint a) const {
        mint res(*this);
        return res /= a;
    }

    friend std::istream &operator>>(std::istream &in, mint &o) {
        ll a;
        in >> a;
        o = a;
        return in;
    }

    friend std::ostream &operator<<(std::ostream &out, const mint &o) {
        out << o.x;
        return out;
    }

};

int dfs(int to, int prev, int k, vector<vector<ll>> &edges, vector<int> &patterns) {
    // 自分とつながっている頂点のうち、すでに色が決まっているもの数
    int determined = 0;
    for (int next : edges[to]) {
        if (patterns[next] == -1) continue;
        determined++;
        for (int next_next : edges[next]) {
            if (patterns[next_next] != -1) determined++;
        }
    }
    int p = k - determined;
    if (p <= 0) return -1;
    patterns[to] = p;
    for (int next : edges[to]) {
        if (next == prev) continue;
        int ok = dfs(next, to, k, edges, patterns);
        if (ok == -1) return -1;
    }
    return 0;
}

