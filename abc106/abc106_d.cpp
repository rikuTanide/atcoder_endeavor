class MatrixSum {
    vector<vector<ll>> sum;
    ll w, h;
public:
    MatrixSum(ll w, ll h) : w(w), h(h) {
        sum = vector<vector<ll>>(w, vector<ll>(h));
    }

    void add(ll x, ll y, ll value) {
        if (x == w || y == h) return;
        sum[x][y] += value;
    }

    ll get(ll x, ll y) {
        if (x == -1 || y == -1) {
            return 0;
        }
        if (x == sum.size() || y == sum[x].size()) {
            return 0;
        }
        return sum[x][y];
    }

    void setUp() {
        for (ll x = 0; x < sum.size(); x++) {
            for (ll y = 0; y < sum[x].size(); y++) {
                sum[x][y] += get(x - 1, y) + get(x, y - 1) - get(x - 1, y - 1);
            }
        }
    }

    ll getSum(ll xs, ll ys, ll xe, ll ye) {
        return get(xe, ye) - get(xs - 1, ye) - get(xe, ys - 1) + get(xs - 1, ys - 1);
    }

};

int main() {
    ll n, m, q;
    cin >> n >> m >> q;

    MatrixSum ms(n, n);

    rep(i, m) {
        ll l, r;
        cin >> l >> r;
        l--;
        r--;

        ms.add(l, r, 1);
    }

    ms.setUp();

    rep(i, q) {
        ll l, r;
        cin >> l >> r;
        l--;
        r--;
        cout << ms.getSum(l, l, r, r) << endl;
    }


}



