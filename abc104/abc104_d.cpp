const int mod = 1000000007;

struct mint {
    ll x; // typedef long long ll;
    mint(ll x = 0) : x((x % mod + mod) % mod) {}

    mint &operator+=(const mint a) {
        if ((x += a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator-=(const mint a) {
        if ((x += mod - a.x) >= mod) x -= mod;
        return *this;
    }

    mint &operator*=(const mint a) {
        (x *= a.x) %= mod;
        return *this;
    }

    mint operator+(const mint a) const {
        mint res(*this);
        return res += a;
    }

    mint operator-(const mint a) const {
        mint res(*this);
        return res -= a;
    }

    mint operator*(const mint a) const {
        mint res(*this);
        return res *= a;
    }

    mint pow(ll t) const {
        if (!t) return 1;
        mint a = pow(t >> 1);
        a *= a;
        if (t & 1) a *= *this;
        return a;
    }

    // for prime mod
    mint inv() const {
        return pow(mod - 2);
    }

    mint &operator/=(const mint a) {
        return (*this) *= a.inv();
    }

    mint operator/(const mint a) const {
        mint res(*this);
        return res /= a;
    }

    friend std::istream &operator>>(std::istream &in, mint &o) {
        ll a;
        in >> a;
        o = a;
        return in;
    }

    friend std::ostream &operator<<(std::ostream &out, const mint &o) {
        out << o.x;
        return out;
    }

};

class CumulativeSum {
    vector<ll> numbers;
    vector<ll> sums;

public:
    CumulativeSum(int n) {
        numbers.resize(n);
        sums.resize(n);
    }

    void set(int i, ll value) {
        numbers[i] = value;
    }

    ll getSum(int i) {
        if (i == -1) return 0;
        if (i == sums.size()) return sums.back();
        return sums[i];
    }

    ll getSectionSum(int start, int end) {
        return getSum(end) - getSum(start - 1);
    }

    void calculate() {
        for (int i = 0; i < numbers.size(); i++) {
            sums[i] = getSum(i - 1) + numbers[i];
        }
    }

};


int main() {

    string s;
    cin >> s;
    int n = s.size();

    CumulativeSum as(n), bs(n), cs(n), hs(n);
    rep(i, n) {
        char c = s[i];
        if (c == 'A') as.set(i, 1);
        if (c == 'B') bs.set(i, 1);
        if (c == 'C') cs.set(i, 1);
        if (c == '?') hs.set(i, 1);
    }
    as.calculate();
    bs.calculate();
    cs.calculate();
    hs.calculate();

    mint ans = 0;
    rep(i, n) {
        if (s[i] != 'B' && s[i] != '?') continue;
        ll a = as.getSectionSum(0, i - 1);
        ll lh = hs.getSectionSum(0, i - 1);
        ll c = cs.getSectionSum(i + 1, n - 1);
        ll rh = hs.getSectionSum(i + 1, n - 1);

        mint l1 = mint(a) * mint(3).pow(lh);
        mint l2 = lh == 0 ? 0 : mint(lh) * mint(3).pow(lh - 1);

        mint r1 = mint(c) * mint(3).pow(rh);
        mint r2 = rh == 0 ? 0 : mint(rh) * mint(3).pow(rh - 1);

        mint l = l1 + l2;
        mint r = r1 + r2;

        mint now = l * r;
        ans += now;
    }

    cout << ans << endl;
}
