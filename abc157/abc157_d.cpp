class UnionFind {
public:
    // 親の番号を格納する。親だった場合-size
    vector<int> parents;

    UnionFind(int n) {
        parents = vector<int>(n, -1);
    }

    // aがどのグループに属しているか
    int root(int a) {
        if (parents[a] < 0) {
            return a;
        }
        return parents[a] = root(parents[a]);
    }

    int size(int a) {
        return -parents[root(a)];
    }

    // aとbをくっつける
    bool connect(int a, int b) {
        int ra = root(a);
        int rb = root(b);
        if (ra == rb) {
            return false;
        }
        // 大きいほうにA
        if (size(ra) < size(rb)) {
            swap(ra, rb);
        }
        parents[ra] += parents[rb];
        parents[rb] = ra;
        return true;
    }

    bool is_union(int a, int b) {
        int ra = root(a);
        int rb = root(b);
        return ra == rb;
    }
};


int main() {

    int n, m, k;
    cin >> n >> m >> k;

    vector<vector<int>> friends(n);
    vector<vector<int>> blocks(n);

    UnionFind uf(n);

    rep(i, m) {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        friends[a].push_back(b);
        friends[b].push_back(a);
        uf.connect(a, b);
    }


    rep(i, k) {
        int a, b;
        cin >> a >> b;
        a--;
        b--;
        blocks[a].push_back(b);
        blocks[b].push_back(a);
    }

    rep(i, n) {
        int size = uf.size(i);
        size--;
        for (int j : friends[i]) {
            if (uf.is_union(i, j)) size--;
        }
        for (int j : blocks[i]) {
            if (uf.is_union(i, j)) size--;
        }
        cout << size << ' ';
    }

    cout << endl;
}
